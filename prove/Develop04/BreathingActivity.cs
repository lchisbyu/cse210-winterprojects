using System;

namespace ActivityProgram
{
    class BreathingActivity
    {
        public void StartActivity()
        {
            Console.WriteLine("Breathing activity started. Please follow the instructions:");
            Console.WriteLine("Sit in a comfortable position and close your eyes.");
            Console.WriteLine("Take a deep breath in for four seconds.");
            Console.WriteLine("Hold your breath for four seconds.");
            Console.WriteLine("Exhale for four seconds.");
            Console.WriteLine("Repeat this process for five minutes.");

            Console.WriteLine("Breathing activity completed.");
        }
    }
}